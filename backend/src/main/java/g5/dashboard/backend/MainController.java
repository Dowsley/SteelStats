package g5.dashboard.backend;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.RestController;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.CrossOrigin;
import org.springframework.web.bind.annotation.PathVariable;
import java.util.*;

@CrossOrigin(origins = "http://localhost:4200")
@RestController // Maps an object to a JSON (that will be sent to the frontend)
@RequestMapping("/demo") // This means URL's start with /demo (after Application path)
public class MainController {
  // This means to get the bean called recordRepository
  // Which is auto-generated by Spring, we will use it to handle the data
  @Autowired
  private RecordRepository recordRepository;
  
  @GetMapping("/equipment")
  public Map<Integer, Double> EquipmentAnnualOees() {
    String code = "REC-T1-LAMTRE-LF03";
    Iterable<Integer> activeYears = recordRepository.findYearsByEquipment(code);
    
    Map<Integer, Double> annualOee = new HashMap<Integer, Double>();
    for (Integer year : activeYears) {
      annualOee.put(year, recordRepository.getAnnualOee(code, year));
    }

    System.out.println(annualOee);
    return annualOee;
  }  
}