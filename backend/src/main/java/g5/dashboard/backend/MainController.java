package g5.dashboard.backend;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.ResponseBody;

@Controller // This means that this class is a Controller
@RequestMapping(path="/demo") // This means URL's start with /demo (after Application path)
public class MainController {
  // This means to get the bean called recordRepository
  // Which is auto-generated by Spring, we will use it to handle the data
  @Autowired
  private RecordRepository recordRepository;

  // Example:
  // localhost:8080/demo/get_annual_oee?code=REC-T1-LAMTRE-LF03&year=2015
  @GetMapping(path="/get_annual_oee")
  public @ResponseBody Double getEquipmentRecords(
  @RequestParam String code, @RequestParam Integer year) {

    return recordRepository.getAnnualOee(code, year);
  }
}